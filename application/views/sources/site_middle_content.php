				<p class="meta">Saterday, March 13, 2010 7:27 AM Posted by <a href="#">Someone</a></p>
				<div class="entry">
				  <p>
                   Whether you wish to start a career in finance, gain recognition for the wealth of skills and experience you
				   have already achieved, or want accelerated progression to the ACCA Qualification and a more senior position
				   in accountancy, ACCA's Certified Accounting Technician (CAT) qualification will help you towards your career
				   of choice.
				  </p>
				  <p>
                   The CAT qualification comprises a combination of exams and practical experience. It is designed to equip you
				   with all the necessary technical skills and knowledge required to fulfill an accounting role to technician level.
				   It also provides a firm foundation of knowledge and the opportunity to fast track towards becoming a professional 
				   ACCA accountant. 				  
				  </p>
