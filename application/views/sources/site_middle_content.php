				<h2 class="title"><a href="#">Association of Chartered Certified Accountants (ACCA)</a></h2>
				<p class="meta">Saterday, March 13, 2010 7:27 AM Posted by <a href="#">Someone</a></p>
				<div class="entry">
				    <p>
                     The Association of Chartered   Certified Accountants (ACCA) is the largest global professional accountancy body, with nearly 300,000 members and students in 160 countries. ACCA's headquarters are in London and it has staffed offices in 31 countries.
					</p>
				    <p>
                     In the UK, ACCA is by far the largest accountancy body in terms of student numbers, and the second largest in terms of members. It is, however, the only body to operate on a global basis. The ACCA syllabus has been recognised by the United Nations as providing the basis for a global accountancy qualification. ACCA members enjoy wide professional mobility.
					</p>
					<p>
                     Founded in 1904 in London, ACCA's mission is to provide quality professional opportunities to people of ability and application, to be a leader in the development of the global accountancy profession, to promote the highest ethical and governance standards and to work in the public interest.
					</p>
